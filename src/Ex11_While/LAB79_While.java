package Ex11_While;

public class LAB79_While {
    public static void main(String[] args) {

        int i = 0; //Initialisation
        while ( i < 10 ){   //Condition
            System.out.println(i);
            i++;   //Updation

        }

        //I,C,U
        for (int j =0; j<=10 ; j++) {
            System.out.println(j);

        }
    }
}
